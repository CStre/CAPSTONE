/** The full catalog — every entry gets a neutral, landmark-free search term. */
export const COUNTRIES: readonly Country[] = CATALOG.map(([code, name]) => ({
  code,
  name,
  searchTerm: term(name),
}));

/** Fast lookup by country code. */
export const COUNTRY_BY_CODE: ReadonlyMap<string, Country> = new Map(
  COUNTRIES.map((c) => [c.code, c]),
);

/** True if a code exists in the catalog. */
export function isValidCountryCode(code: string): boolean {
  return COUNTRY_BY_CODE.has(code);
}
